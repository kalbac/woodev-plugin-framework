<?php
/**
 * Dev tool: regenerate woodev/class-map.php by scanning the framework tree.
 *
 * Usage: php bin/generate-class-map.php
 * NOT shipped — lives in bin/, never required at runtime.
 *
 * @package Woodev\Framework
 */

declare( strict_types=1 );

$root      = dirname( __DIR__ );
$framework = $root . '/woodev';

// Files loaded explicitly (bootstrap / loader / the autoloader+map themselves) — never autoloaded.
$skip = [
	'woodev/bootstrap.php',
	'woodev/loader.php',
	'woodev/class-map.php',
	'woodev/class-framework-autoloader.php',
];

$map = [];

$iterator = new RecursiveIteratorIterator(
	new RecursiveDirectoryIterator( $framework, FilesystemIterator::SKIP_DOTS )
);

/** @var SplFileInfo $file */
foreach ( $iterator as $file ) {
	if ( 'php' !== strtolower( $file->getExtension() ) ) {
		continue;
	}

	$relative = str_replace( '\\', '/', substr( $file->getPathname(), strlen( $root ) + 1 ) );

	if ( in_array( $relative, $skip, true ) ) {
		continue;
	}

	$symbols = woodev_extract_symbols( (string) file_get_contents( $file->getPathname() ) );

	foreach ( $symbols as $fqcn ) {
		if ( isset( $map[ $fqcn ] ) ) {
			fwrite( STDERR, sprintf( "DUPLICATE class %s in %s and %s\n", $fqcn, $map[ $fqcn ], $relative ) );
			exit( 1 );
		}
		$map[ $fqcn ] = $relative;
	}
}

ksort( $map );

$export  = "<?php\n";
$export .= "/**\n * GENERATED by bin/generate-class-map.php — do not edit by hand.\n";
$export .= " * Maps framework class/interface/trait FQCN => path relative to the framework copy root.\n";
$export .= " *\n * @package Woodev\\Framework\n */\n\n";
$export .= "return [\n";
foreach ( $map as $fqcn => $path ) {
	$export .= sprintf( "\t%s => %s,\n", var_export( $fqcn, true ), var_export( $path, true ) );
}
$export .= "];\n";

file_put_contents( $framework . '/class-map.php', $export );

fwrite( STDOUT, sprintf( "Wrote %d entries to woodev/class-map.php\n", count( $map ) ) );

/**
 * Extracts fully-qualified class/interface/trait names from PHP source via tokens.
 *
 * @param string $source PHP source.
 * @return string[] FQCNs.
 */
function woodev_extract_symbols( string $source ): array {
	$tokens    = token_get_all( $source );
	$namespace = '';
	$symbols   = [];
	$count     = count( $tokens );

	for ( $i = 0; $i < $count; $i++ ) {
		$token = $tokens[ $i ];

		if ( is_array( $token ) && T_NAMESPACE === $token[0] ) {
			$namespace = '';
			for ( $j = $i + 1; $j < $count; $j++ ) {
				$next = $tokens[ $j ];
				if ( ';' === $next || '{' === $next ) {
					break;
				}
				if ( is_array( $next ) && in_array( $next[0], [ T_STRING, T_NS_SEPARATOR ], true ) ) {
					$namespace .= $next[1];
				}
				if ( is_array( $next ) && defined( 'T_NAME_QUALIFIED' ) && T_NAME_QUALIFIED === $next[0] ) {
					$namespace .= $next[1];
				}
			}
			continue;
		}

		if ( is_array( $token ) && in_array( $token[0], [ T_CLASS, T_INTERFACE, T_TRAIT ], true ) ) {
			// Skip `::class` and anonymous classes.
			$prev = $tokens[ $i - 1 ] ?? null;
			if ( is_array( $prev ) && T_DOUBLE_COLON === $prev[0] ) {
				continue;
			}
			for ( $j = $i + 1; $j < $count; $j++ ) {
				$next = $tokens[ $j ];
				if ( is_array( $next ) && T_WHITESPACE === $next[0] ) {
					continue;
				}
				if ( is_array( $next ) && T_STRING === $next[0] ) {
					$symbols[] = '' !== $namespace ? $namespace . '\\' . $next[1] : $next[1];
				}
				break;
			}
		}
	}

	return $symbols;
}
